import type { CashFlowMonth, MsmeProfile } from '../lib/types'

const MONTHS = ['Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun']

/** Deterministic 12-month cash-flow series from a base level + seasonality. */
function genCashflow(inflow: number, outflow: number, vol: number, growth: number, seed: number): CashFlowMonth[] {
  let balance = Math.round(inflow * 0.35)
  return MONTHS.map((month, i) => {
    const seasonal = 1 + vol * Math.sin((i / 12) * Math.PI * 2 + seed)
    const trend = 1 + (growth / 100) * ((i - 5.5) / 12)
    const inf = Math.max(0, Math.round((inflow * seasonal * trend) / 1000) * 1000)
    const outf = Math.max(0, Math.round((outflow * (1 + vol * 0.45 * Math.sin((i / 12) * Math.PI * 2 + seed + 1))) / 1000) * 1000)
    balance = Math.round((balance + inf - outf) / 1000) * 1000
    return { month, inflow: inf, outflow: outf, balance }
  })
}

